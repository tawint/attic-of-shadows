/// <summary>
/// Типы монстров.
/// Быстрые: линии (верт/гориз), макс 10 на волну.
/// Жирные: галочки, линии, макс 7 на волну.
/// Стреляющий: линии; у пули свои HP (линии, макс 3), пока пуля жива — монстра не убить.
/// Босс: звездочка и много других символов, медленный.
/// </summary>
public enum EnemyType
{
    Fast,    // быстрые — линии, макс 10 на волну
    Tanky,   // жирные — галочки, линии, макс 7 на волну
    Shooting,// стреляющий — линии; пуля 3 HP (линии), пока жива — монстра не убить
    Boss     // босс — звезда + др. символы, медленный
}
